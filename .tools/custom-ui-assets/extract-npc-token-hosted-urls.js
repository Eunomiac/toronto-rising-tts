#!/usr/bin/env node
"use strict";

// Pairs hosted Steam URLs for tokenFront_<key> + tokenBack_<key> after merge/upload.
// Reads .dev/custom-ui-assets/generated-assets.json and/or save CustomUIAssets.

const fs = require("fs");
const path = require("path");
const { resolveSavePath } = require("../tts-save/resolve-save-path");

const FRONT_NAME_RE = /^tokenFront_([A-Za-z0-9_]+)$/i;
const BACK_NAME_RE = /^tokenBack_([A-Za-z0-9_]+)$/i;

/**
 * @param {string[]} argv
 * @returns {Record<string, string>}
 */
function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (!token.startsWith("--")) {
      continue;
    }
    const key = token.slice(2);
    const next = argv[i + 1];
    if (next === undefined || next.startsWith("--")) {
      args[key] = "1";
    } else {
      args[key] = next;
      i += 1;
    }
  }
  return args;
}

/**
 * @param {string} filePath
 * @returns {unknown}
 */
function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

/**
 * @param {string} outputPath
 */
function ensureParentDir(outputPath) {
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
}

/**
 * @param {string} value
 * @returns {string}
 */
function escapeForLuaString(value) {
  return value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

/**
 * @param {unknown} value
 * @returns {value is string}
 */
function isHostedSteamUrl(value) {
  if (typeof value !== "string") {
    return false;
  }
  return /^https?:\/\/(?:cloud-\d+\.steamusercontent\.com|steamusercontent-a\.akamaihd\.net)\/ugc\//i.test(value);
}

/**
 * @param {Map<string, string>} map
 * @returns {boolean}
 */
function mapHasNpcTokenAssetNames(map) {
  for (const name of map.keys()) {
    if (FRONT_NAME_RE.test(name) || BACK_NAME_RE.test(name)) {
      return true;
    }
  }
  return false;
}

/**
 * Merge hosted URLs from a save's CustomUIAssets / CustomAssets table.
 * @param {Map<string, string>} map
 * @param {string} savePath
 */
function mergeNameToUrlFromSaveCustomAssets(map, savePath) {
  if (!savePath || !fs.existsSync(savePath)) {
    return;
  }

  const saveRoot = readJson(savePath);
  if (saveRoot === null || typeof saveRoot !== "object" || Array.isArray(saveRoot)) {
    throw new Error("Save root must be an object.");
  }
  const root = /** @type {Record<string, unknown>} */ (saveRoot);
  const assets = Array.isArray(root.CustomUIAssets)
    ? root.CustomUIAssets
    : Array.isArray(root.CustomAssets)
      ? root.CustomAssets
      : [];

  for (const entry of assets) {
    if (!entry || typeof entry !== "object") {
      continue;
    }
    const row = /** @type {Record<string, unknown>} */ (entry);
    const name = row.Name;
    const url = row.URL;
    if (typeof name === "string" && isHostedSteamUrl(url)) {
      map.set(name, url);
    }
  }
}

/**
 * @param {Map<string, string>} map
 * @param {string} generatedAssetsPath
 */
function mergeNpcRowsFromGeneratedFile(map, generatedAssetsPath) {
  if (!generatedAssetsPath || !fs.existsSync(generatedAssetsPath)) {
    return;
  }
  const raw = readJson(generatedAssetsPath);
  if (!Array.isArray(raw)) {
    throw new Error(`generated-assets must be an array: ${generatedAssetsPath}`);
  }
  for (const entry of raw) {
    if (!entry || typeof entry !== "object") {
      continue;
    }
    const name = entry.Name;
    const url = entry.URL;
    if (
      typeof name === "string"
      && (FRONT_NAME_RE.test(name) || BACK_NAME_RE.test(name))
      && isHostedSteamUrl(url)
    ) {
      map.set(name, url);
    }
  }
}

/**
 * Load name → URL map from NPC merge output, save CustomUIAssets, or legacy generated-assets.
 * Site-card-only generated-assets.json must not block NPC extract.
 * @param {string} generatedAssetsPath
 * @param {string} savePath
 * @param {string} fallbackGeneratedPath
 * @returns {Map<string, string>}
 */
function loadNameToUrl(generatedAssetsPath, savePath, fallbackGeneratedPath) {
  /** @type {Map<string, string>} */
  const map = new Map();

  mergeNpcRowsFromGeneratedFile(map, generatedAssetsPath);

  if (!mapHasNpcTokenAssetNames(map)) {
    mergeNameToUrlFromSaveCustomAssets(map, savePath);
  }

  if (!mapHasNpcTokenAssetNames(map)) {
    mergeNpcRowsFromGeneratedFile(map, fallbackGeneratedPath);
  }

  return map;
}

/**
 * @param {Record<string, { front: string; back: string }>} complete
 * @returns {string}
 */
function buildLuaModule(complete) {
  const keys = Object.keys(complete).sort((a, b) => a.localeCompare(b, "en"));
  const lines = keys.map((characterKey) => {
    const row = complete[characterKey];
    return [
      `  ${characterKey} = {`,
      `    front = "${escapeForLuaString(row.front)}",`,
      `    back = "${escapeForLuaString(row.back)}",`,
      "  },",
    ].join("\n");
  });

  return [
    "-- Auto-generated by .tools/custom-ui-assets/extract-npc-token-hosted-urls.js",
    "-- Steam-hosted URLs for NPC control-board tokens (ImageURL / ImageSecondaryURL).",
    "local M = {",
    ...lines,
    "}",
    "",
    "return M",
    "",
  ].join("\n");
}

/**
 * @param {Record<string, string>} args
 * @returns {string}
 */
function resolveSavePathFromArgs(args) {
  if (args.save) {
    return path.resolve(args.save);
  }
  if (args.saveName) {
    return resolveSavePath(args.saveName.trim(), args.savesDir).savePath;
  }
  return path.resolve(".dev/TS_Save_230.json");
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const generatedAssetsPath = path.resolve(
    args.generatedAssets || ".dev/custom-ui-assets/npc-group-generated-assets.json",
  );
  const fallbackGeneratedPath = path.resolve(".dev/custom-ui-assets/npc-generated-assets.json");
  const savePath = resolveSavePathFromArgs(args);
  console.log(`Save: ${savePath}`);
  const jsonOut = path.resolve(args.out || ".dev/custom-ui-assets/npc-token-hosted-urls.json");
  const luaOut = path.resolve(args.luaOut || "lib/npc_token_hosted_urls.ttslua");

  const nameToUrl = loadNameToUrl(generatedAssetsPath, savePath, fallbackGeneratedPath);

  if (!mapHasNpcTokenAssetNames(nameToUrl)) {
    throw new Error(
      "No hosted token URLs found. After upload + save, run:\n"
        + "  npm run custom-ui-assets:merge-npc-tokens\n"
        + "Then re-run extract (or pass --save <your>.json if not .dev/TS_Save_230.json).",
    );
  }

  /** @type {Map<string, { front?: string; back?: string }>} */
  const paired = new Map();

  for (const [name, url] of nameToUrl.entries()) {
    const frontMatch = FRONT_NAME_RE.exec(name);
    if (frontMatch) {
      const key = frontMatch[1];
      const row = paired.get(key) || {};
      row.front = url;
      paired.set(key, row);
      continue;
    }
    const backMatch = BACK_NAME_RE.exec(name);
    if (backMatch) {
      const key = backMatch[1];
      const row = paired.get(key) || {};
      row.back = url;
      paired.set(key, row);
    }
  }

  if (paired.size === 0) {
    throw new Error(
      'No tokenFront_* / tokenBack_* asset names in URL map. Use the NPC token manifest and merge before extract.',
    );
  }

  /** @type {string[]} */
  const incomplete = [];
  /** @type {Record<string, { front: string; back: string }>} */
  const complete = {};

  for (const [characterKey, row] of paired.entries()) {
    if (!isHostedSteamUrl(row.front) || !isHostedSteamUrl(row.back)) {
      incomplete.push(characterKey);
      continue;
    }
    complete[characterKey] = {
      front: row.front,
      back: row.back,
    };
  }

  const output = {
    generatedAt: new Date().toISOString(),
    source: "extract-npc-token-hosted-urls",
    characterCount: Object.keys(complete).length,
    incompleteCharacterKeys: incomplete.sort((a, b) => a.localeCompare(b, "en")),
    byCharacterKey: complete,
  };

  ensureParentDir(jsonOut);
  fs.writeFileSync(jsonOut, `${JSON.stringify(output, null, 2)}\n`, "utf8");
  ensureParentDir(luaOut);
  fs.writeFileSync(luaOut, buildLuaModule(complete), "utf8");

  console.log(`Wrote paired URLs JSON: ${jsonOut}`);
  console.log(`Wrote Lua lookup module: ${luaOut}`);
  console.log(`Complete pairs: ${Object.keys(complete).length}`);
  if (incomplete.length > 0) {
    console.log(`Incomplete (missing front or back URL): ${incomplete.length}`);
    for (const key of incomplete.slice(0, 20)) {
      console.log(`  - ${key}`);
    }
    if (incomplete.length > 20) {
      console.log(`  ... and ${incomplete.length - 20} more`);
    }
    process.exitCode = 2;
  }
}

main();
