#!/usr/bin/env node
"use strict";

// Agent guidance: dev/TTS_BUNDLING_SETUP.md; outputs consumed by lib/custom_ui_upload_manifest.ttslua (see dev/custom-ui-assets/).

const fs = require("fs");
const path = require("path");

/**
 * Parse CLI args of shape: --key value
 * @param {string[]} argv
 * @returns {Record<string, string>}
 */
function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (!token.startsWith("--")) {
      continue;
    }
    const key = token.slice(2);
    const value = argv[i + 1];
    if (!value || value.startsWith("--")) {
      throw new Error(`Missing value for argument "${token}"`);
    }
    args[key] = value;
    i += 1;
  }
  return args;
}

/**
 * Validate a filename stem can be used as a TTS custom asset name.
 * @param {string} stem
 * @returns {boolean}
 */
function isValidAssetName(stem) {
  return /^[A-Za-z0-9._-]+$/.test(stem);
}

/**
 * Escape a string for safe inclusion in a Lua double-quoted literal.
 * @param {string} value
 * @returns {string}
 */
function escapeForLuaString(value) {
  return value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

/**
 * Convert an absolute Windows path to a file:// URL recognized by TTS.
 * @param {string} absoluteFilePath
 * @returns {string}
 */
function toFileUrl(absoluteFilePath) {
  const normalized = absoluteFilePath.replace(/\\/g, "/");
  return `file:///${normalized}`;
}

/**
 * Ensure destination directory exists.
 * @param {string} outputPath
 */
function ensureParentDir(outputPath) {
  const parentDir = path.dirname(outputPath);
  fs.mkdirSync(parentDir, { recursive: true });
}

/**
 * Build a Lua block containing imageList and imageNames arrays.
 * @param {{name:string,fileUrl:string}[]} assets
 * @returns {string}
 */
function buildLuaArrays(assets) {
  const imageListLines = assets.map((asset) => `  "${escapeForLuaString(asset.fileUrl)}",`);
  const imageNameLines = assets.map((asset) => `  "${escapeForLuaString(asset.name)}",`);
  const manifestAssetLines = assets.map(
    (asset) =>
      `    { name = "${escapeForLuaString(asset.name)}", fileUrl = "${escapeForLuaString(asset.fileUrl)}" },`,
  );

  return [
    "-- Auto-generated by tools/custom-ui-assets/build-upload-manifest.js",
    "-- Paste into your TTS Global script.",
    "-- Supports both legacy imageList/imageNames and DEBUG manifest batch spawning.",
    "imageList =",
    "{",
    ...imageListLines,
    "}",
    "",
    "imageNames =",
    "{",
    ...imageNameLines,
    "}",
    "",
    "customUiUploadManifest = {",
    "  assets = {",
    ...manifestAssetLines,
    "  }",
    "}",
    "",
    "-- Optional console usage once pasted into Global:",
    '-- lua DEBUG.spawnCustomUiUploadBatchFromManifest(customUiUploadManifest, {columns = 8, gap = 2, startY = 3})',
    '-- lua DEBUG.clearCustomUiUploadTokens()',
    "",
  ].join("\n");
}

/**
 * Build a require-able Lua module that returns the upload manifest table.
 * @param {{name:string,fileUrl:string}[]} assets
 * @returns {string}
 */
function buildLuaModule(assets) {
  const manifestAssetLines = assets.map(
    (asset) =>
      `    { name = "${escapeForLuaString(asset.name)}", fileUrl = "${escapeForLuaString(asset.fileUrl)}" },`,
  );

  return [
    "-- Auto-generated by tools/custom-ui-assets/build-upload-manifest.js",
    "-- Required by DEBUG.loadCustomUiUploadManifest()",
    "local M = {",
    "  assets = {",
    ...manifestAssetLines,
    "  }",
    "}",
    "",
    "return M",
    "",
  ].join("\n");
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const inputDir = args.input;
  const outputPath = args.out;
  const luaOut = args.luaOut || "";
  const moduleOut = args.moduleOut || "";
  const extensionsCsv = args.extensions || "webp";

  if (!inputDir) {
    throw new Error("Required argument missing: --input <directory>");
  }
  if (!outputPath) {
    throw new Error("Required argument missing: --out <manifest.json>");
  }

  const absoluteInputDir = path.resolve(inputDir);
  if (!fs.existsSync(absoluteInputDir) || !fs.statSync(absoluteInputDir).isDirectory()) {
    throw new Error(`Input directory does not exist: ${absoluteInputDir}`);
  }

  const allowedExtensions = new Set(
    extensionsCsv
      .split(",")
      .map((ext) => ext.trim().toLowerCase())
      .filter((ext) => ext.length > 0),
  );

  if (allowedExtensions.size === 0) {
    throw new Error("No valid extensions specified via --extensions");
  }

  const dirEntries = fs.readdirSync(absoluteInputDir, { withFileTypes: true });
  const files = dirEntries
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b, "en"));

  const assets = [];
  const stemToFile = new Map();
  const invalidStems = [];

  for (const fileName of files) {
    const ext = path.extname(fileName).slice(1).toLowerCase();
    if (!allowedExtensions.has(ext)) {
      continue;
    }

    const stem = path.parse(fileName).name;
    if (!isValidAssetName(stem)) {
      invalidStems.push(stem);
      continue;
    }

    if (stemToFile.has(stem)) {
      throw new Error(
        `Duplicate filename stem "${stem}" detected in "${absoluteInputDir}" (files: "${stemToFile.get(stem)}", "${fileName}")`,
      );
    }
    stemToFile.set(stem, fileName);

    const absolutePath = path.resolve(absoluteInputDir, fileName);
    assets.push({
      name: stem,
      sourcePath: absolutePath,
      fileUrl: toFileUrl(absolutePath),
    });
  }

  if (assets.length === 0) {
    throw new Error(
      `No image files found in "${absoluteInputDir}" with extensions: ${Array.from(allowedExtensions).join(", ")}`,
    );
  }
  if (invalidStems.length > 0) {
    throw new Error(
      `Invalid filename stems detected (allowed: letters, numbers, dot, underscore, hyphen): ${invalidStems.join(", ")}`,
    );
  }

  const outputManifest = {
    generatedAt: new Date().toISOString(),
    inputDirectory: absoluteInputDir,
    count: assets.length,
    assets,
  };

  const absoluteOutputPath = path.resolve(outputPath);
  ensureParentDir(absoluteOutputPath);
  fs.writeFileSync(absoluteOutputPath, `${JSON.stringify(outputManifest, null, 2)}\n`, "utf8");

  if (luaOut) {
    const absoluteLuaOut = path.resolve(luaOut);
    ensureParentDir(absoluteLuaOut);
    fs.writeFileSync(absoluteLuaOut, buildLuaArrays(assets), "utf8");
  }
  if (moduleOut) {
    const absoluteModuleOut = path.resolve(moduleOut);
    ensureParentDir(absoluteModuleOut);
    fs.writeFileSync(absoluteModuleOut, buildLuaModule(assets), "utf8");
  }

  console.log(`Manifest written: ${absoluteOutputPath}`);
  if (luaOut) {
    console.log(`Lua arrays written: ${path.resolve(luaOut)}`);
  }
  if (moduleOut) {
    console.log(`Lua module written: ${path.resolve(moduleOut)}`);
  }
  console.log(`Assets discovered: ${assets.length}`);
  console.log("");
  console.log("NEXT (TTS):");
  console.log("1) Run Save & Play so DEBUG reloads the generated module.");
  console.log("2) In TTS console, run:");
  console.log("   lua DEBUG.spawnCustomUiUploadBatch()");
  console.log("3) In Cloud Manager, click Upload All Loaded Files.");
  console.log("4) Save the game.");
  console.log("");
  console.log("Then run task: Custom UI Assets: Merge Hosted URLs into Save CustomUIAssets");
}

main();
