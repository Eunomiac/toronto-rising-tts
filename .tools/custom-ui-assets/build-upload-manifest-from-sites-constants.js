#!/usr/bin/env node
"use strict";

// Agent guidance: .dev/custom-ui-assets/; pairs with run-custom-ui-assets-pipeline.js (--mode sites-from-constants).
// Reads `C.Sites` in lib/constants.ttslua: `image` = TTS Custom UI asset name (spawn `Name`); `localImage` = file URL/path.
// Batching: `--batch` (default max 40) or `--batchMax N`; alphabetical slice by C.Sites table key; `--batchStart Key` or TTY prompt.

const fs = require("fs");
const path = require("path");
const readline = require("readline");
const { fileURLToPath } = require("url");

/** Default batch size when `--batch` is set without `--batchMax`. */
const HARDCODED_SITE_UPLOAD_BATCH_MAX = 40;

/**
 * Parse CLI args: `--key value` or boolean `--flag` (value "1").
 * @param {string[]} argv
 * @returns {Record<string, string>}
 */
function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (!token.startsWith("--")) {
      continue;
    }
    const key = token.slice(2);
    const next = argv[i + 1];
    if (next === undefined || next.startsWith("--")) {
      args[key] = "1";
    } else {
      args[key] = next;
      i += 1;
    }
  }
  return args;
}

/**
 * @param {string} question
 * @returns {Promise<string>}
 */
function promptLine(question) {
  return new Promise((resolve) => {
    const rl = readline.createInterface({
      input: process.stdin,
      output: process.stdout,
    });
    rl.question(question, (answer) => {
      rl.close();
      resolve((answer || "").trim());
    });
  });
}

/**
 * @param {{ siteKey: string }[]} sortedBySiteKey
 * @param {string} startKey
 * @returns {number}
 */
function findBatchStartIndex(sortedBySiteKey, startKey) {
  if (!startKey) {
    return 0;
  }
  const idx = sortedBySiteKey.findIndex((r) => r.siteKey.localeCompare(startKey, "en") >= 0);
  if (idx === -1) {
    return sortedBySiteKey.length;
  }
  return idx;
}

/**
 * @param {string} stem
 * @returns {boolean}
 */
function isValidAssetName(stem) {
  return /^[A-Za-z0-9._-]+$/.test(stem);
}

/**
 * @param {string} value
 * @returns {string}
 */
function escapeForLuaString(value) {
  return value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

/**
 * @param {string} outputPath
 */
function ensureParentDir(outputPath) {
  const parentDir = path.dirname(outputPath);
  fs.mkdirSync(parentDir, { recursive: true });
}

/**
 * @param {{name:string,fileUrl:string}[]} assets
 * @returns {string}
 */
function buildLuaArrays(assets) {
  const imageListLines = assets.map((asset) => `  "${escapeForLuaString(asset.fileUrl)}",`);
  const imageNameLines = assets.map((asset) => `  "${escapeForLuaString(asset.name)}",`);
  const manifestAssetLines = assets.map(
    (asset) =>
      `    { name = "${escapeForLuaString(asset.name)}", fileUrl = "${escapeForLuaString(asset.fileUrl)}" },`,
  );

  return [
    "-- Auto-generated by .tools/custom-ui-assets/build-upload-manifest-from-sites-constants.js",
    "-- Paste into your TTS Global script.",
    "-- Supports both legacy imageList/imageNames and DEBUG manifest batch spawning.",
    "imageList =",
    "{",
    ...imageListLines,
    "}",
    "",
    "imageNames =",
    "{",
    ...imageNameLines,
    "}",
    "",
    "customUiUploadManifest = {",
    "  assets = {",
    ...manifestAssetLines,
    "  }",
    "}",
    "",
    "-- Optional console usage once pasted into Global:",
    '-- lua DEBUG.spawnCustomUiUploadBatchFromManifest(customUiUploadManifest, {columns = 8, gap = 2, startY = 3})',
    '-- lua DEBUG.clearCustomUiUploadTokens()',
    "",
  ].join("\n");
}

/**
 * @param {{name:string,fileUrl:string}[]} assets
 * @returns {string}
 */
function buildLuaModule(assets) {
  const manifestAssetLines = assets.map(
    (asset) =>
      `    { name = "${escapeForLuaString(asset.name)}", fileUrl = "${escapeForLuaString(asset.fileUrl)}" },`,
  );

  return [
    "-- Auto-generated by .tools/custom-ui-assets/build-upload-manifest-from-sites-constants.js",
    "-- Required by DEBUG.loadCustomUiUploadManifest()",
    "local M = {",
    "  assets = {",
    ...manifestAssetLines,
    "  }",
    "}",
    "",
    "return M",
    "",
  ].join("\n");
}

/**
 * Slice inner text of `C.Sites = { ... }` using brace depth, skipping `--` comments and quoted strings.
 * @param {string} fullText
 * @returns {string}
 */
function extractSitesTableInner(fullText) {
  const marker = "C.Sites = ";
  const markerIdx = fullText.indexOf(marker);
  if (markerIdx === -1) {
    throw new Error('Could not find "C.Sites = " in constants file.');
  }

  let i = markerIdx + marker.length;
  while (i < fullText.length && /\s/.test(fullText[i])) {
    i += 1;
  }
  if (fullText[i] !== "{") {
    throw new Error('Expected "{" immediately after C.Sites =');
  }

  let depth = 0;
  const startBrace = i;

  for (; i < fullText.length; i += 1) {
    const c = fullText[i];
    const c2 = fullText[i + 1];

    if (c === "-" && c2 === "-") {
      i += 2;
      while (i < fullText.length && fullText[i] !== "\n" && fullText[i] !== "\r") {
        i += 1;
      }
      continue;
    }

    if (c === '"') {
      i += 1;
      while (i < fullText.length) {
        const ch = fullText[i];
        if (ch === "\\") {
          i += 2;
          continue;
        }
        if (ch === '"') {
          break;
        }
        i += 1;
      }
      continue;
    }

    if (c === "'") {
      i += 1;
      while (i < fullText.length) {
        const ch = fullText[i];
        if (ch === "\\") {
          i += 2;
          continue;
        }
        if (ch === "'") {
          break;
        }
        i += 1;
      }
      continue;
    }

    if (c === "{") {
      depth += 1;
    } else if (c === "}") {
      depth -= 1;
      if (depth === 0) {
        return fullText.slice(startBrace + 1, i);
      }
    }
  }

  throw new Error("Unclosed C.Sites table (brace mismatch).");
}

/**
 * @typedef {{ siteKey: string; image: string; localImage: string }} SiteAssetRow
 */

/**
 * @param {string} inner
 * @returns {SiteAssetRow[]}
 */
function parseSiteEntries(inner) {
  /** @type {SiteAssetRow[]} */
  const rows = [];
  const headerRe = /^  ([A-Za-z0-9_]+) = \{\r?\n/gm;
  /** @type {{ siteKey: string; start: number }[]} */
  const headers = [];
  let m = headerRe.exec(inner);
  while (m !== null) {
    headers.push({ siteKey: m[1], start: m.index });
    m = headerRe.exec(inner);
  }

  for (let h = 0; h < headers.length; h += 1) {
    const { siteKey, start } = headers[h];
    const end = h + 1 < headers.length ? headers[h + 1].start : inner.length;
    const chunk = inner.slice(start, end);

    const imageMatch = chunk.match(/^\s*image\s*=\s*"([^"]*)"/m);
    const localMatch = chunk.match(/^\s*localImage\s*=\s*"([^"]*)"/m);

    const image = imageMatch ? imageMatch[1].trim() : "";
    const localImage = localMatch ? localMatch[1].trim() : "";

    rows.push({ siteKey, image, localImage });
  }

  return rows;
}

/**
 * Build a `file:///` URL for TTS: forward slashes only, no URI percent-encoding.
 * (Node `pathToFileURL` percent-encodes spaces and symbols; TTS expects literals as in constants.)
 * @param {string} absoluteFilePath
 * @returns {string}
 */
function absoluteOsPathToFileUrl(absoluteFilePath) {
  const normalized = absoluteFilePath.replace(/\\/g, "/");
  return `file:///${normalized}`;
}

/**
 * @param {string} localImageRaw
 * @returns {{ fileUrl: string; fsPath: string } | { error: string }}
 */
function resolveLocalImagePaths(localImageRaw) {
  const raw = (localImageRaw || "").trim();
  if (raw === "") {
    return { error: "empty localImage" };
  }

  if (raw.startsWith("file:")) {
    try {
      const fsPath = fileURLToPath(raw);
      // Emit the same string as in `C.Sites.localImage`; do not re-canonicalize with pathToFileURL.
      return { fileUrl: raw, fsPath };
    } catch (e) {
      const msg = e instanceof Error ? e.message : String(e);
      return { error: `Invalid file URL (${msg}): ${raw}` };
    }
  }

  const abs = path.isAbsolute(raw) ? path.normalize(raw) : path.resolve(raw);
  const fileUrl = absoluteOsPathToFileUrl(abs);
  if (!fileUrl.startsWith("file:///")) {
    return { error: `File URL builder did not yield file:/// (${fileUrl})` };
  }
  return { fileUrl, fsPath: abs };
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const constantsPath = path.resolve(args.constants || "lib/constants.ttslua");
  const outputPath = path.resolve(args.out || ".dev/custom-ui-assets/manifest.json");
  const luaOut = args.luaOut ? path.resolve(args.luaOut) : "";
  const moduleOut = args.moduleOut ? path.resolve(args.moduleOut) : "";
  const skipMissing = args.skipMissing === "1" || args.skipMissing === "true";

  const batched = args.batch === "1" || args.batch === "true";
  let batchMax = parseInt(String(args.batchMax || "0"), 10);
  if (Number.isNaN(batchMax) || batchMax < 0) {
    batchMax = 0;
  }
  if (batched && batchMax === 0) {
    batchMax = HARDCODED_SITE_UPLOAD_BATCH_MAX;
  }

  if (!fs.existsSync(constantsPath)) {
    throw new Error(`Constants file not found: ${constantsPath}`);
  }

  const fullText = fs.readFileSync(constantsPath, "utf8");
  const inner = extractSitesTableInner(fullText);
  const parsed = parseSiteEntries(inner);

  if (parsed.length === 0) {
    throw new Error("No site entries parsed from C.Sites (check formatting / indentation).");
  }

  /** @type {{name:string,fileUrl:string,sourcePath:string,siteKey:string}[]} */
  const assetsAll = [];
  /** @type {string[]} */
  const warnings = [];
  /** @type {string[]} */
  const errors = [];
  /** @type {Map<string, string>} */
  const nameToSiteKey = new Map();

  for (const row of parsed) {
    const rawImage = (row.image || "").trim();
    const manifestName = rawImage !== "" ? rawImage : `siteCard_${row.siteKey}`;
    if (!isValidAssetName(manifestName)) {
      errors.push(
        `Site "${row.siteKey}": invalid image / derived name "${manifestName}" (allowed: letters, numbers, ._-)`,
      );
      continue;
    }
    if (row.localImage === "") {
      if (skipMissing) {
        warnings.push(`Site "${row.siteKey}": no localImage, skipped`);
      } else {
        errors.push(`Site "${row.siteKey}": missing localImage = "..."`);
      }
      continue;
    }

    const resolved = resolveLocalImagePaths(row.localImage);
    if ("error" in resolved) {
      if (skipMissing) {
        warnings.push(`Site "${row.siteKey}": ${resolved.error}, skipped`);
      } else {
        errors.push(`Site "${row.siteKey}": ${resolved.error}`);
      }
      continue;
    }

    if (!fs.existsSync(resolved.fsPath)) {
      const msg = `file not found: ${resolved.fsPath}`;
      if (skipMissing) {
        warnings.push(`Site "${row.siteKey}": ${msg}, skipped`);
      } else {
        errors.push(`Site "${row.siteKey}": ${msg}`);
      }
      continue;
    }

    if (nameToSiteKey.has(manifestName)) {
      errors.push(
        `Duplicate manifest name "${manifestName}" (sites "${nameToSiteKey.get(manifestName)}" and "${row.siteKey}")`,
      );
      continue;
    }
    nameToSiteKey.set(manifestName, row.siteKey);

    assetsAll.push({
      name: manifestName,
      fileUrl: resolved.fileUrl,
      sourcePath: resolved.fsPath,
      siteKey: row.siteKey,
    });
  }

  for (const w of warnings) {
    console.warn(`WARN: ${w}`);
  }

  if (errors.length > 0) {
    const cap = 25;
    const shown = errors.slice(0, cap);
    const extra = errors.length > cap ? `\n... and ${errors.length - cap} more` : "";
    throw new Error(`Site manifest validation failed:\n${shown.join("\n")}${extra}`);
  }

  if (assetsAll.length === 0) {
    throw new Error("No assets after filtering; fix paths or drop --skipMissing to see errors.");
  }

  assetsAll.sort((a, b) => a.siteKey.localeCompare(b.siteKey, "en"));

  let batchStartArg = (args.batchStart || "").trim();
  /** @type {{name:string,fileUrl:string,sourcePath:string,siteKey:string}[]} */
  let assets;

  if (batchMax > 0 && assetsAll.length > batchMax) {
    if (!batchStartArg && process.stdin.isTTY) {
      batchStartArg = await promptLine(
        `Validated ${assetsAll.length} site image files. This manifest will include at most ${batchMax} (alphabetical C.Sites keys).\nEnter C.Sites table key to start (empty = first key): `,
      );
    }
    if (!batchStartArg && !process.stdin.isTTY) {
      throw new Error(
        `Refusing to write a partial manifest: ${assetsAll.length} sites exceed --batchMax ${batchMax}. ` +
          `Pass --batchStart <SiteKey> (C.Sites identifier, e.g. ROMC5Lounge), set --batchMax 0 to emit all sites in one manifest, or run interactively for a prompt.`,
      );
    }
    const startIdx = findBatchStartIndex(assetsAll, batchStartArg);
    assets = assetsAll.slice(startIdx, startIdx + batchMax);
    if (assets.length === 0) {
      throw new Error(
        `No sites in this batch window (--batchStart "${batchStartArg}" → start index ${startIdx}). Check the key spelling.`,
      );
    }
    const nextIdx = startIdx + assets.length;
    if (nextIdx < assetsAll.length) {
      const nextKey = assetsAll[nextIdx].siteKey;
      console.log("");
      console.log(`Next batch: re-run with --batchStart ${nextKey} (same --batch / --batchMax).`);
    } else {
      console.log("");
      console.log("This batch reaches the last site key; no further --batchStart needed for this pass.");
    }
  } else {
    assets = assetsAll;
  }

  const outputManifest = {
    generatedAt: new Date().toISOString(),
    source: "sites-from-constants",
    constantsPath,
    count: assets.length,
    totalSitesValidated: assetsAll.length,
    siteUploadBatch:
      batchMax > 0
        ? {
            batchMax,
            batchStartFilter: batchStartArg || null,
            firstSiteKey: assets[0].siteKey,
            lastSiteKey: assets[assets.length - 1].siteKey,
          }
        : undefined,
    assets,
  };

  ensureParentDir(outputPath);
  fs.writeFileSync(outputPath, `${JSON.stringify(outputManifest, null, 2)}\n`, "utf8");

  if (luaOut) {
    ensureParentDir(luaOut);
    fs.writeFileSync(luaOut, buildLuaArrays(assets), "utf8");
  }
  if (moduleOut) {
    ensureParentDir(moduleOut);
    fs.writeFileSync(moduleOut, buildLuaModule(assets), "utf8");
  }

  console.log(`Manifest written: ${outputPath}`);
  if (luaOut) {
    console.log(`Lua arrays written: ${luaOut}`);
  }
  if (moduleOut) {
    console.log(`Lua module written: ${moduleOut}`);
  }
  console.log(
    `Site assets in this manifest: ${assets.length} (validated ${assetsAll.length} C.Sites rows with local files)`,
  );
  console.log("");
  console.log("NEXT (TTS):");
  console.log("1) Run Save & Play so DEBUG reloads the generated module.");
  console.log("2) In TTS console, run:");
  console.log("   lua DEBUG.spawnCustomUiUploadBatch()");
  console.log("3) In Cloud Manager, click Upload All Loaded Files.");
  console.log("4) Save the game.");
  console.log("");
  console.log("Then merge hosted URLs (Custom UI Assets: Merge Hosted URLs into Save CustomUIAssets).");
}

main().catch((err) => {
  console.error(err instanceof Error ? err.message : String(err));
  process.exit(1);
});
