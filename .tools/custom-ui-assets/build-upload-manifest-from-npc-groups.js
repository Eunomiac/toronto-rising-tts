#!/usr/bin/env node
"use strict";

// Agent guidance: .dev/custom-ui-assets/README.md (NPC unified groups).
// Scans assets/images/NPCs for complete 4-file character groups, skips groups
// already hosted in save CustomUIAssets, emits manifest + lib/npc_group_upload_manifest.ttslua.

const fs = require("fs");
const path = require("path");
const readline = require("readline");
const { resolveSavePath } = require("../tts-save/resolve-save-path");
const {
  DEFAULT_NPC_GROUP_IMAGE_DIR,
  HARDCODED_NPC_GROUP_BATCH_CHAR_MAX,
  ensureParentDir,
  escapeForLuaString,
  extractCharacterKeysFromNpcsData,
  readCustomUiAssetMap,
  isNpcGroupFullyHosted,
  scanNpcGroupsInDirectory,
  findBatchStartIndex,
  groupToManifestAssets,
} = require("./lib/npc-asset-helpers");

/**
 * @param {string[]} argv
 * @returns {Record<string, string>}
 */
function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (!token.startsWith("--")) {
      continue;
    }
    const key = token.slice(2);
    const next = argv[i + 1];
    if (next === undefined || next.startsWith("--")) {
      args[key] = "1";
    } else {
      args[key] = next;
      i += 1;
    }
  }
  return args;
}

/**
 * @param {string} question
 * @returns {Promise<string>}
 */
function promptLine(question) {
  return new Promise((resolve) => {
    const rl = readline.createInterface({
      input: process.stdin,
      output: process.stdout,
    });
    rl.question(question, (answer) => {
      rl.close();
      resolve((answer || "").trim());
    });
  });
}

/**
 * @param {{ name: string; fileUrl: string; sourcePath: string; characterKey: string; kind: string }[]} assets
 * @returns {string}
 */
function buildLuaModule(assets) {
  const manifestAssetLines = assets.map(
    (asset) =>
      `    { name = "${escapeForLuaString(asset.name)}", fileUrl = "${escapeForLuaString(asset.fileUrl)}", characterKey = "${escapeForLuaString(asset.characterKey)}", kind = "${escapeForLuaString(asset.kind)}" },`,
  );

  return [
    "-- Auto-generated by .tools/custom-ui-assets/build-upload-manifest-from-npc-groups.js",
    "-- Required by DEBUG.loadCustomUiUploadManifest(\"lib.npc_group_upload_manifest\")",
    "local M = {",
    '  source = "npc-groups",',
    "  assets = {",
    ...manifestAssetLines,
    "  }",
    "}",
    "",
    "return M",
    "",
  ].join("\n");
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const inputDir = (args.dir || args.input || DEFAULT_NPC_GROUP_IMAGE_DIR).trim();
  const npcsDataPath = path.resolve(args.npcsData || "lib/npcs_data.ttslua");
  const outputPath = path.resolve(args.out || ".dev/custom-ui-assets/npc-group-manifest.json");
  const moduleOut = path.resolve(args.moduleOut || "lib/npc_group_upload_manifest.ttslua");
  const warnUnknownKeys = args.warnUnknownKeys === "1" || args.warnUnknownKeys === "true";
  const skipSaveCheck = args.skipSaveCheck === "1" || args.skipSaveCheck === "true";

  const dirPath = path.isAbsolute(inputDir) ? path.normalize(inputDir) : path.resolve(inputDir);
  console.log(`NPC group image directory: ${dirPath}`);
  if (!fs.existsSync(dirPath) || !fs.statSync(dirPath).isDirectory()) {
    throw new Error(
      `NPC group image directory not found: ${dirPath}\n` +
        `Create it with <name>.webp, <name>Back.webp, tokenFront_<name>.webp, tokenBack_<name>.webp (default: ${DEFAULT_NPC_GROUP_IMAGE_DIR}).`,
    );
  }

  const { groups, errors } = scanNpcGroupsInDirectory(dirPath);
  if (groups.length === 0 && errors.length === 0) {
    throw new Error(`No complete NPC 4-file groups found in: ${dirPath}`);
  }
  if (errors.length > 0) {
    const cap = 25;
    const shown = errors.slice(0, cap);
    const extra = errors.length > cap ? `\n... and ${errors.length - cap} more` : "";
    throw new Error(`NPC group scan failed:\n${shown.join("\n")}${extra}`);
  }

  /** @type {Set<string> | null} */
  let knownCharacterKeys = null;
  if (fs.existsSync(npcsDataPath)) {
    const npcsText = fs.readFileSync(npcsDataPath, "utf8");
    knownCharacterKeys = extractCharacterKeysFromNpcsData(npcsText);
  }

  if (warnUnknownKeys && knownCharacterKeys !== null) {
    for (const group of groups) {
      if (!knownCharacterKeys.has(group.characterKey)) {
        console.warn(
          `WARN: "${group.characterKey}" not in D.characters (${path.basename(npcsDataPath)}) — will still include in manifest`,
        );
      }
    }
  }

  /** @type {import("./lib/npc-asset-helpers").NpcAssetGroup[]} */
  let pendingGroups = groups;
  /** @type {string[]} */
  const alreadyHostedKeys = [];

  const saveArg = (args.save || "").trim();
  const saveNameArg = (args.saveName || "").trim();
  if (!skipSaveCheck && (saveArg || saveNameArg)) {
    const savePath = saveArg
      ? path.resolve(saveArg)
      : resolveSavePath(saveNameArg, args.savesDir).savePath;
    if (!fs.existsSync(savePath)) {
      throw new Error(`Save file not found for hosted check: ${savePath}`);
    }
    const saveRoot = JSON.parse(fs.readFileSync(savePath, "utf8"));
    const assetMap = readCustomUiAssetMap(saveRoot);
    pendingGroups = [];
    for (const group of groups) {
      if (isNpcGroupFullyHosted(assetMap, group.characterKey)) {
        alreadyHostedKeys.push(group.characterKey);
      } else {
        pendingGroups.push(group);
      }
    }
    console.log(
      `Save hosted check: ${groups.length} total groups, ${alreadyHostedKeys.length} already hosted, ${pendingGroups.length} pending`,
    );
  }

  if (pendingGroups.length === 0) {
    const outputManifest = {
      generatedAt: new Date().toISOString(),
      source: "npc-groups",
      inputDirectory: dirPath,
      count: 0,
      characterCount: 0,
      totalGroupsScanned: groups.length,
      alreadyHostedCount: alreadyHostedKeys.length,
      alreadyHostedKeys,
      pendingCharacterKeys: [],
      assets: [],
    };
    ensureParentDir(outputPath);
    fs.writeFileSync(outputPath, `${JSON.stringify(outputManifest, null, 2)}\n`, "utf8");
    ensureParentDir(moduleOut);
    fs.writeFileSync(moduleOut, buildLuaModule([]), "utf8");
    console.log(`Manifest written (empty — nothing pending): ${outputPath}`);
    console.log(`Lua module written: ${moduleOut}`);
    return;
  }

  const batched = args.batch === "1" || args.batch === "true";
  let batchCharMax = parseInt(String(args.batchMax || "0"), 10);
  if (Number.isNaN(batchCharMax) || batchCharMax < 0) {
    batchCharMax = 0;
  }
  if (batched && batchCharMax === 0) {
    batchCharMax = HARDCODED_NPC_GROUP_BATCH_CHAR_MAX;
  }

  let batchStartArg = (args.batchStart || "").trim();
  /** @type {import("./lib/npc-asset-helpers").NpcAssetGroup[]} */
  let sliceGroups = pendingGroups;

  if (batchCharMax > 0 && pendingGroups.length > batchCharMax) {
    if (!batchStartArg && process.stdin.isTTY) {
      batchStartArg = await promptLine(
        `Pending ${pendingGroups.length} NPC groups. This manifest includes at most ${batchCharMax} characters (4 assets each).\nEnter characterKey to start (empty = first pending): `,
      );
    }
    if (!batchStartArg && !process.stdin.isTTY) {
      throw new Error(
        `Refusing to write a partial manifest: ${pendingGroups.length} pending groups exceed --batchMax ${batchCharMax}. ` +
          `Pass --batchStart <characterKey>, set --batchMax 0 for all groups, or run interactively.`,
      );
    }
    const startIdx = findBatchStartIndex(pendingGroups, batchStartArg);
    sliceGroups = pendingGroups.slice(startIdx, startIdx + batchCharMax);
    if (sliceGroups.length === 0) {
      throw new Error(
        `No characters in this batch window (--batchStart "${batchStartArg}" → index ${startIdx}). Check spelling.`,
      );
    }
    const nextIdx = startIdx + sliceGroups.length;
    if (nextIdx < pendingGroups.length) {
      console.log("");
      console.log(`Next batch: re-run with --batchStart ${pendingGroups[nextIdx].characterKey}`);
    } else {
      console.log("");
      console.log("This batch reaches the last pending character key; no further --batchStart needed.");
    }
  }

  /** @type {{ name: string; fileUrl: string; sourcePath: string; characterKey: string; kind: string }[]} */
  const assets = [];
  for (const group of sliceGroups) {
    assets.push(...groupToManifestAssets(group));
  }

  const outputManifest = {
    generatedAt: new Date().toISOString(),
    source: "npc-groups",
    inputDirectory: dirPath,
    count: assets.length,
    characterCount: sliceGroups.length,
    totalGroupsScanned: groups.length,
    alreadyHostedCount: alreadyHostedKeys.length,
    alreadyHostedKeys,
    totalPendingGroups: pendingGroups.length,
    pendingCharacterKeys: sliceGroups.map((g) => g.characterKey),
    npcGroupBatch:
      batchCharMax > 0
        ? {
            batchCharMax,
            batchStartFilter: batchStartArg || null,
            firstCharacterKey: sliceGroups[0].characterKey,
            lastCharacterKey: sliceGroups[sliceGroups.length - 1].characterKey,
          }
        : undefined,
    assets,
  };

  ensureParentDir(outputPath);
  fs.writeFileSync(outputPath, `${JSON.stringify(outputManifest, null, 2)}\n`, "utf8");
  ensureParentDir(moduleOut);
  fs.writeFileSync(moduleOut, buildLuaModule(assets), "utf8");

  console.log(`Manifest written: ${outputPath}`);
  console.log(`Lua module written: ${moduleOut}`);
  console.log(
    `NPC group assets in this manifest: ${assets.length} files (${sliceGroups.length} characters × 4)`,
  );
  if (knownCharacterKeys !== null) {
    console.log(`D.characters registry has ${knownCharacterKeys.size} keys in ${path.basename(npcsDataPath)}`);
  }
  console.log("");
  console.log("NEXT (TTS):");
  console.log("1) Save & Play");
  console.log(
    '2) lua DEBUG.spawnNpcGroupUploadBatch({ columns = 12, gap = 2, startY = 3 })',
  );
  console.log('   (or lua DEBUG.spawnCustomUiUploadBatch(opts, "lib.npc_group_upload_manifest"))');
  console.log("3) Cloud Manager → Upload All Loaded Files → Save game");
  console.log("4) Merge + extract + registry gap report:");
  console.log("   npm run custom-ui-assets:merge-npc-groups");
  console.log("   npm run custom-ui-assets:extract-npc-token-urls");
  console.log("   npm run custom-ui-assets:report-npc-registry-gaps");
}

main().catch((err) => {
  console.error(err instanceof Error ? err.message : String(err));
  process.exit(1);
});
